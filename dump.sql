name VARCHAR(255) DEFAULT '',
meta_title VARCHAR(255) DEFAULT '',
meta_description TEXT,
content TEXT,
status INT(11) DEFAULT 0,
image VARCHAR(255) DEFAULT '',
PRIMARY KEY (id)
);

